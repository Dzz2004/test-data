"""
Trajectory Generator - 根据计划生成完整轨迹内容

使用 LLM 生成每个节点的具体内容（input/output/rationale）。
"""
import json
from typing import Any
from .domain import Domain
from .llm_client import chat, chat_json
from .mock_tools import mock_tool_call


def generate_trajectory(
    plan: dict,
    domain: Domain,
) -> dict:
    """
    Generate a complete trajectory from a plan.
    Each node gets concrete content generated by LLM.
    """
    task = plan["task"]
    profile = plan["user_profile"]
    planned_nodes = plan["planned_nodes"]

    skill_map = {s.skill_id: s for s in domain.skills}
    tool_map = {t.tool_id: t for t in domain.tools}

    nodes = []
    edges = []
    context_so_far = []  # Running context for LLM

    for i, node_plan in enumerate(planned_nodes):
        node_type = node_plan["node_type"]
        node_name = node_plan["node_name"]
        node_id = f"node_{i:03d}"

        if node_type == "user_input":
            node = _generate_user_input_node(
                node_id, node_name, i, task, profile, context_so_far
            )
        elif node_type == "tool_call":
            tool_id = node_plan.get("tool_id") or node_name
            tool_spec = tool_map.get(tool_id)
            node = _generate_tool_call_node(
                node_id, node_name, tool_id, tool_spec, task, profile, context_so_far
            )
        elif node_type == "skill_call":
            skill_id = node_plan.get("skill_id") or node_name
            skill_spec = skill_map.get(skill_id)
            node = _generate_skill_call_node(
                node_id, node_name, skill_id, skill_spec, task, profile, context_so_far
            )
        elif node_type == "agent_response":
            node = _generate_final_response_node(
                node_id, task, profile, context_so_far
            )
        elif node_type == "agent_reasoning":
            node = _generate_reasoning_node(
                node_id, node_name, task, profile, context_so_far
            )
        else:
            node = {
                "node_id": node_id,
                "node_type": node_type,
                "name": node_name,
                "content": f"[{node_name}]",
            }

        nodes.append(node)
        context_so_far.append({"node_id": node_id, "type": node_type, "name": node_name, "summary": _summarize_node(node)})

        # Generate edge from previous node
        if i > 0:
            edges.append({
                "from": f"node_{i-1:03d}",
                "to": node_id,
                "relation": _generate_edge_relation(nodes[i-1], node),
            })

    # Extract final response
    final_response = ""
    for n in reversed(nodes):
        if n["node_type"] == "agent_response":
            final_response = n.get("content", n.get("output", ""))
            break

    return {
        "trajectory_id": plan["trajectory_id"],
        "domain": domain.spec.domain_id,
        "template_used": plan["template_id"],
        "user_profile": profile,
        "task": task,
        "nodes": nodes,
        "edges": edges,
        "final_response": final_response,
    }


def _generate_user_input_node(
    node_id: str, node_name: str, index: int,
    task: dict, profile: dict, context: list,
) -> dict:
    """Generate user input node content."""
    if index == 0:
        # First node: use the task's initial query
        content = task["initial_user_query"]
    else:
        # Follow-up: generate based on context
        context_summary = "\n".join(f"- {c['name']}: {c['summary']}" for c in context[-3:])
        prompt = f"""用户画像:
- 角色: {profile.get('currentRole', '')}
- 目标: {profile.get('shortTermGoal', '')}
- 交互风格: {task.get('interaction_style', '信息充分型')}

之前的对话上下文:
{context_summary}

当前节点类型: {node_name}
请生成用户在此处会说的话（自然语言，30-80字，不加引号）。

如果是 user_followup: 用户补充之前缺失的信息。
如果是 user_constraint_update: 用户修改了某个约束条件。"""

        content = chat([{"role": "user", "content": prompt}], temperature=0.8, max_tokens=150).strip().strip('"')

    return {
        "node_id": node_id,
        "node_type": "user_input",
        "name": node_name,
        "content": content,
    }


def _generate_tool_call_node(
    node_id: str, node_name: str, tool_id: str,
    tool_spec: Any, task: dict, profile: dict, context: list,
) -> dict:
    """Generate a tool call node with mock results."""
    # Determine tool input based on profile and context
    tool_input = _infer_tool_input(tool_id, profile, task, context)

    # Get mock output
    tool_output = mock_tool_call(tool_id, tool_input)

    # Generate rationale
    tool_name = tool_spec.name if tool_spec else node_name
    rationale = f"根据用户的目标（{profile.get('targetRole', '')}），需要调用{tool_name}获取相关信息。"

    return {
        "node_id": node_id,
        "node_type": "tool_call",
        "name": node_name,
        "tool_id": tool_id,
        "input": tool_input,
        "output": tool_output,
        "rationale": rationale,
    }


def _generate_skill_call_node(
    node_id: str, node_name: str, skill_id: str,
    skill_spec: Any, task: dict, profile: dict, context: list,
) -> dict:
    """Generate a skill call node using LLM."""
    skill_name = skill_spec.name if skill_spec else node_name
    skill_desc = skill_spec.description if skill_spec else ""
    skill_outputs = skill_spec.outputs if skill_spec else ["result"]

    context_summary = "\n".join(f"- {c['name']}: {c['summary']}" for c in context[-4:])

    prompt = f"""你是一个职业发展 AI Agent 的内部推理模块。当前正在执行 "{skill_name}" 技能。

技能说明: {skill_desc}
期望输出字段: {', '.join(skill_outputs)}

用户画像:
- 当前角色: {profile.get('currentRole', '')}
- 目标角色: {profile.get('targetRole', '')}
- 经验: {profile.get('experienceSummary', '')[:100]}
- 约束: {', '.join(profile.get('constraints', [])[:3])}

任务: {task.get('task_description', '')}
用户原始请求: {task.get('initial_user_query', '')[:100]}

前序节点:
{context_summary}

请生成此技能节点的输出，格式为 JSON:
{{
  "output": {{ ... 具体输出内容 ... }},
  "rationale": "执行此技能的理由（一句话）"
}}"""

    try:
        result = chat_json([{"role": "user", "content": prompt}], temperature=0.5)
        output = result.get("output", result)
        rationale = result.get("rationale", f"执行 {skill_name} 以推进任务。")
    except Exception:
        output = {field: f"[{skill_name} output for {field}]" for field in skill_outputs}
        rationale = f"执行 {skill_name} 以推进任务。"

    return {
        "node_id": node_id,
        "node_type": "skill_call",
        "name": node_name,
        "skill_id": skill_id,
        "input": {"context": context_summary, "profile_summary": profile.get("currentRole", "")},
        "output": output,
        "rationale": rationale,
    }


def _generate_final_response_node(
    node_id: str, task: dict, profile: dict, context: list,
) -> dict:
    """Generate the final agent response."""
    context_summary = "\n".join(f"- {c['name']}: {c['summary']}" for c in context)

    prompt = f"""你是一个职业发展 AI 助手，请根据以下分析过程为用户生成最终回复。

用户请求: {task.get('initial_user_query', '')}
用户背景: {profile.get('currentRole', '')}，目标 {profile.get('targetRole', '')}

分析过程:
{context_summary}

请生成一个结构化的最终回复（200-400字），包含具体的建议和下一步行动。语言与用户一致。"""

    content = chat([{"role": "user", "content": prompt}], temperature=0.6, max_tokens=800)

    return {
        "node_id": node_id,
        "node_type": "agent_response",
        "name": "final_response",
        "content": content.strip(),
    }


def _generate_reasoning_node(
    node_id: str, node_name: str, task: dict, profile: dict, context: list,
) -> dict:
    """Generate an agent reasoning/clarification node."""
    context_summary = "\n".join(f"- {c['name']}: {c['summary']}" for c in context[-3:])

    prompt = f"""Agent 需要向用户提出澄清问题。

用户原始请求: {task.get('initial_user_query', '')}
交互风格: {task.get('interaction_style', '')}
前序分析: {context_summary}

生成 agent 的追问（1-2 个具体问题，帮助明确用户需求）。直接输出追问内容。"""

    content = chat([{"role": "user", "content": prompt}], temperature=0.7, max_tokens=200)

    return {
        "node_id": node_id,
        "node_type": "agent_reasoning",
        "name": node_name,
        "content": content.strip(),
    }


def _infer_tool_input(tool_id: str, profile: dict, task: dict, context: list) -> dict:
    """Infer tool input parameters from profile and context."""
    if tool_id == "job_requirement_search":
        return {
            "job_title": profile.get("targetRole", "开发工程师"),
            "seniority": _infer_seniority(profile),
            "region": profile.get("locationRegion", ""),
        }
    elif tool_id == "tech_trend_search":
        # Pick a technology from the expected skills
        skills = task.get("expected_skills", ["Python"])
        return {"technology": skills[0] if skills else "Python", "aspect": "demand"}
    elif tool_id == "course_resource_search":
        skills = task.get("expected_skills", ["Python"])
        return {"skill_name": skills[0] if skills else "Python", "difficulty": "intermediate"}
    elif tool_id == "project_repository_search":
        return {"tech_stack": task.get("expected_skills", [])[:3], "difficulty": "intermediate"}
    elif tool_id == "interview_question_search":
        return {"role": profile.get("targetRole", ""), "category": "system_design", "difficulty": "medium"}
    elif tool_id == "salary_benchmark_search":
        return {"role": profile.get("targetRole", ""), "seniority": _infer_seniority(profile), "region": profile.get("locationRegion", "")}
    elif tool_id == "knowledge_graph_query":
        skills = task.get("expected_skills", ["Python"])
        return {"target_skill": skills[0] if skills else "Python", "depth": 2}
    else:
        return {"query": task.get("task_description", "")}


def _infer_seniority(profile: dict) -> str:
    """Infer seniority from profile."""
    role = profile.get("currentRole", "").lower()
    if any(w in role for w in ["应届", "毕业", "实习", "junior", "entry"]):
        return "junior"
    elif any(w in role for w in ["专家", "架构", "senior", "staff", "P7", "P8"]):
        return "senior"
    else:
        return "mid"


def _summarize_node(node: dict) -> str:
    """Create a brief summary of a node for context passing."""
    node_type = node["node_type"]
    if node_type == "user_input":
        return node.get("content", "")[:80]
    elif node_type == "tool_call":
        output = node.get("output", {})
        if isinstance(output, dict):
            keys = list(output.keys())[:3]
            return f"工具返回: {', '.join(keys)}"
        return str(output)[:80]
    elif node_type == "skill_call":
        output = node.get("output", {})
        if isinstance(output, dict):
            keys = list(output.keys())[:3]
            return f"技能输出: {', '.join(keys)}"
        return str(output)[:80]
    elif node_type == "agent_response":
        return node.get("content", "")[:80]
    elif node_type == "agent_reasoning":
        return node.get("content", "")[:80]
    return node.get("name", "")


def _generate_edge_relation(from_node: dict, to_node: dict) -> str:
    """Generate a relation description between two nodes."""
    from_name = from_node.get("name", from_node.get("node_type", ""))
    to_name = to_node.get("name", to_node.get("node_type", ""))
    return f"{from_name} 的输出被 {to_name} 使用"
